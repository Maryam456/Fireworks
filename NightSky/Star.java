class Star {
  private float x;
  private float y;
  private float radius;

  Star(float xParameter, float yParameter, float radiusParameter) {
    x = xParameter;
    y = yParameter;
    radius = radiusParameter;
  } //End of Constructor

  // Getters
  public float getX() {
    return x;
  }
  public float getY() {
    return y;
  }
  public float getRadius() {
    return radius;
  }
}
